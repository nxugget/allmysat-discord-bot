const countries: Record<string, CountryEntry> = {};

for (const [code, name] of Object.entries(raw)) {
  countries[code] = { name, flag: toFlag(code) };
}

/**
 * Resolve an ISO alpha-2 code to its display name and flag emoji.
 * Returns the raw code if unknown.
 */
export function formatCountry(code: string): string {
  const entry = countries[code.toUpperCase()];
  if (!entry) return code;
  return `${entry.flag} ${entry.name}`;
}
