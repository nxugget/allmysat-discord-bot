interface CountryEntry {
  name: string;
  flag: string;
}

function toFlag(code: string): string {
  return String.fromCodePoint(
    ...Array.from(code.toUpperCase()).map((c) => 0x1f1e6 + c.charCodeAt(0) - 65)
  );
}

